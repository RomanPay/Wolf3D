#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	char const *tmp;

	tmp = s;
	if (s)
	{
		while (*tmp)
		{
			ft_putchar_fd(*tmp, fd);
			tmp++;
		}
	}
}
